      end

    now = DateTime.utc_now() |> DateTime.to_iso8601()

    rows =
      Enum.map_join(goals, "\n", fn g ->
        "| #{g.title} | #{g.status || "—"} | #{g.timeframe || "—"} |"
      end)

    content = """
    # Goals

    Auto-generated by EMA SystemBrain. Do not edit manually.

    **Count:** #{length(goals)}

    | Title | Status | Timeframe |
    |-------|--------|-----------|
    #{rows}

    ---
    *Last synced: #{now}*
    """

    File.write!(Path.join(state_dir, "goals.md"), content)
  end

  defp write_habits_state(state_dir) do
    habits =
      try do
        Ema.Habits.list_active()
      rescue
        _ -> []
      end

    now = DateTime.utc_now() |> DateTime.to_iso8601()

    rows =
      Enum.map_join(habits, "\n", fn h ->
        "| #{h.name} | #{h.frequency || "daily"} |"
      end)

    content = """
