  defp write_responsibilities_state(state_dir) do
    resps =
      try do
        Ema.Responsibilities.list_responsibilities()
      rescue
        _ -> []
      end

    now = DateTime.utc_now() |> DateTime.to_iso8601()

    rows =
      Enum.map_join(resps, "\n", fn r ->
        health =
          if r.health, do: "#{Float.round(r.health * 100, 0) |> trunc()}%", else: "—"

        "| #{r.title} | #{r.role || "—"} | #{r.cadence || "—"} | #{health} |"
      end)

    content = """
    # Responsibilities

    Auto-generated by EMA SystemBrain. Do not edit manually.

    **Count:** #{length(resps)}

    | Title | Role | Cadence | Health |
    |-------|------|---------|--------|
    #{rows}

    ---
    *Last synced: #{now}*
    """

    File.write!(Path.join(state_dir, "responsibilities.md"), content)
  end
end
